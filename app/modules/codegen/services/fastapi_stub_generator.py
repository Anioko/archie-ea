# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _slugify(name: str) -> str:
    """Convert a name to a lowercase underscore-separated identifier."""
    slug = re.sub(r"[^a-z0-9]+", "_", (name or "component").lower()).strip("_")
    return slug or "component"


def _to_class_name(name: str) -> str:
    """Convert a name to PascalCase."""
    return "".join(
        word.capitalize()
        for word in re.split(r"[^a-z0-9]+", (name or "Component").lower())
        if word
    ) or "Component"


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def generate(solution_id: int) -> dict:
    """Generate a deterministic FastAPI scaffold for *solution_id*.

    Queries the database for ApplicationComponent and DataObject ArchiMate
    elements linked to the solution, then renders each file using Python
    string.Template (no Jinja2, no LLM, reproducible).

    Returns
    -------
    dict[str, str]
        Mapping of *filename* → *file content* for all generated files.
        Always includes ``main.py``, ``docker-compose.yml``, and ``README.md``.
    """
    components = _load_components(solution_id)
    data_objects = _load_data_objects(solution_id)

    if not components:
        # Emit a minimal scaffold even with no components so callers always
        # receive a valid (if empty) bundle.
        components = [{"name": "DefaultService", "slug": "default_service"}]

    return _render(solution_id, components, data_objects)


# ---------------------------------------------------------------------------
# DB loading helpers
# ---------------------------------------------------------------------------

def _load_components(solution_id: int) -> list:
    """Return a list of {name, slug} dicts for application-layer elements."""
    try:
        from app.models.solution_archimate_element import SolutionArchiMateElement

        rows = SolutionArchiMateElement.query.filter_by(
            solution_id=solution_id,
            layer_type="application",
        ).all()

        seen_slugs: set = set()
        components = []
        for row in rows:
            name = (row.element_name or "").strip() or f"Component{row.id}"
            slug = _slugify(name)
            # Deduplicate slugs
            if slug in seen_slugs:
                slug = f"{slug}_{row.id}"
            seen_slugs.add(slug)
            components.append({"name": name, "slug": slug, "id": row.id})

        return components
    except Exception as exc:  # noqa: BLE001
        logger.warning("Could not load application components: %s", exc)
        return []


def _load_data_objects(solution_id: int) -> list:
    """Return a list of {name, slug, cls} dicts for data-layer elements."""
    try:
        from app.models.solution_archimate_element import SolutionArchiMateElement

        # DataObjects use layer_type "data" in the ArchiMate model
        rows = SolutionArchiMateElement.query.filter(
            SolutionArchiMateElement.solution_id == solution_id,
            SolutionArchiMateElement.layer_type.in_(["data", "data_object", "DataObject"]),
        ).all()

        seen_slugs: set = set()
        objects = []
        for row in rows:
            name = (row.element_name or "").strip() or f"DataObject{row.id}"
            slug = _slugify(name)
            if slug in seen_slugs:
                slug = f"{slug}_{row.id}"
            seen_slugs.add(slug)
            objects.append({"name": name, "slug": slug, "cls": _to_class_name(name)})

        return objects
    except Exception as exc:  # noqa: BLE001
        logger.warning("Could not load data objects: %s", exc)
        return []


# ---------------------------------------------------------------------------
# Rendering
# ---------------------------------------------------------------------------

def _render(solution_id: int, components: list, data_objects: list) -> dict:
    """Render all template files and return the {filename: content} dict."""
    files: dict = {}

    # ── Router files ──────────────────────────────────────────────────────
    for comp in components:
        slug = comp["slug"]
        name = comp["name"]
        content = _ROUTER_T.substitute(
            title=name,
            slug=slug,
            fn=slug,
        )
        files[f"routers/{slug}.py"] = content

    # ── Pydantic model files ───────────────────────────────────────────────
    for obj in data_objects:
        content = _MODEL_T.substitute(
            title=obj["name"],
            cls=obj["cls"],
        )
        files[f"models/{obj['slug']}.py"] = content

    # ── main.py ───────────────────────────────────────────────────────────
    import_lines = "\n".join(
        f"from routers.{c['slug']} import router as {c['slug']}_router"
        for c in components
    )
    include_lines = "\n".join(
        f"app.include_router({c['slug']}_router)"
        for c in components
    )
    component_list_str = ", ".join(c["name"] for c in components)
    files["main.py"] = _MAIN_T.substitute(
        solution_id=solution_id,
        component_list=component_list_str,
        imports=import_lines,
        includes=include_lines,
    )

    # ── docker-compose.yml ────────────────────────────────────────────────
    base_port = 8000
    service_blocks = []
    for i, comp in enumerate(components):
        service_blocks.append(
            _DOCKER_SERVICE_T.substitute(
                slug=comp["slug"],
                port=base_port + i,
            )
        )
    files["docker-compose.yml"] = _DOCKER_COMPOSE_T.substitute(
        services="\n".join(service_blocks),
    )

    # ── README.md ─────────────────────────────────────────────────────────
    from datetime import datetime, timezone

    comp_rows = "\n".join(
        f"| {c['name']} | `routers/{c['slug']}.py` | port {8000 + i} |"
        for i, c in enumerate(components)
    )
    components_table = (
        "| Component | Router file | Port |\n"
        "|-----------|-------------|------|\n"
        + comp_rows
    )
    router_tree = "\n".join(f"├── {c['slug']}.py" for c in components)
    model_tree = (
        "\n".join(f"├── {obj['slug']}.py" for obj in data_objects)
        if data_objects
        else "(none — add DataObject elements in the ArchiMate composer)"
    )

    files["README.md"] = _README_T.substitute(
        solution_id=solution_id,
        timestamp=datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
        components_table=components_table,
        router_tree=router_tree,
        model_tree=model_tree,
    )

    return files


def generate_zip(solution_id: int) -> bytes:
    """Generate the FastAPI scaffold and return it as an in-memory ZIP archive."""
    files = generate(solution_id)
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        for filename, content in files.items():
            zf.writestr(filename, content)
    return buf.getvalue()
