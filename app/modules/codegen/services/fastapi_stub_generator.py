"""
FastAPI stub generator — deterministic code generation from ArchiMate elements.

Reads ApplicationComponent and DataObject elements linked to a solution and
produces a runnable FastAPI scaffold using Python string.Template (no Jinja2,
no LLM, fully reproducible).

Public API:
    generate(solution_id) -> dict[str, str]   # {filename: content}
"""

import io
import logging
import re
import textwrap
import zipfile
from string import Template

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Template strings (Python string.Template uses $var / ${var} syntax)
# ---------------------------------------------------------------------------

_ROUTER_T = Template(
    textwrap.dedent("""\
    \"\"\"
    FastAPI router for ${title} — deterministic scaffold.
    Replace the 501 stubs with real implementations.
    \"\"\"
    from fastapi import APIRouter, HTTPException, status
    from typing import Any, List

    router = APIRouter(prefix="/${slug}", tags=["${title}"])


    @router.get("/", response_model=List[Any], summary="List ${title}")
    async def list_${fn}():
        raise HTTPException(
            status_code=status.HTTP_501_NOT_IMPLEMENTED,
            detail="${title}: list not yet implemented",
        )


    @router.get("/{item_id}", response_model=Any, summary="Get ${title}")
    async def get_${fn}(item_id: int):
        raise HTTPException(
            status_code=status.HTTP_501_NOT_IMPLEMENTED,
            detail="${title}: get not yet implemented",
        )


    @router.post("/", response_model=Any, status_code=status.HTTP_201_CREATED,
                 summary="Create ${title}")
    async def create_${fn}(body: Any):
        raise HTTPException(
            status_code=status.HTTP_501_NOT_IMPLEMENTED,
            detail="${title}: create not yet implemented",
        )


    @router.put("/{item_id}", response_model=Any, summary="Update ${title}")
    async def update_${fn}(item_id: int, body: Any):
        raise HTTPException(
            status_code=status.HTTP_501_NOT_IMPLEMENTED,
            detail="${title}: update not yet implemented",
        )


    @router.delete("/{item_id}", status_code=status.HTTP_204_NO_CONTENT,
                   summary="Delete ${title}")
    async def delete_${fn}(item_id: int):
        raise HTTPException(
            status_code=status.HTTP_501_NOT_IMPLEMENTED,
            detail="${title}: delete not yet implemented",
        )
    """)
)

