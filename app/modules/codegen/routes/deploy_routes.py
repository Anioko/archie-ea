@codegen_bp.route(
    "/solutions/<int:solution_id>/codegen/deploy-cloud", methods=["POST"]
)
@login_required
def deploy_to_coolify(solution_id):
    """Deploy generated code to Coolify-managed infrastructure."""
    solution = Solution.query.get_or_404(solution_id)
    _check_access(solution)

    gen = CodegenGeneration.query.filter_by(solution_id=solution_id).first()
    if not gen or not gen.generated_files:
        return jsonify({"error": "No generated code found. Run code generation first."}), 400

    language = gen.config.get("language", "python-fastapi") if gen.config else "python-fastapi"
    code_files = gen.generated_files
    if not isinstance(code_files, dict):
        return jsonify({"error": "Generated files format invalid."}), 400

    try:
        from app.modules.codegen.services.instance_tracker import InstanceTracker
        from app.modules.codegen.services.deployment_orchestrator import DeploymentOrchestrator

        tracker = InstanceTracker()
        tracker.check_quota()

        orch = DeploymentOrchestrator()
        instance = orch.deploy(
            solution_id=solution_id,
            language=language,
            code_files=code_files,
        )
        return jsonify({
            "status": "deploying",
            "deployment_url": instance.deployment_url,
            "version": instance.version,
            "instance_id": instance.id,
            "message": "Your solution is being deployed. Check status for updates.",
        }), 202

    except RuntimeError as e:
        return jsonify({"error": str(e)}), 503


@codegen_bp.route(
    "/solutions/<int:solution_id>/codegen/deploy-cloud/status", methods=["GET"]
)
@login_required
def deploy_status(solution_id):
    """Check deployment status for a solution."""
    solution = Solution.query.get_or_404(solution_id)
    _check_access(solution)

    from app.modules.codegen.services.instance_tracker import InstanceTracker

    tracker = InstanceTracker()
    instance = tracker.get_instance(solution_id=solution_id)
    if not instance:
        return jsonify({"status": "not_deployed", "message": "No deployment found for this solution."}), 404

    if instance.health_status == "deploying":
        try:
            from app.modules.codegen.services.deployment_orchestrator import DeploymentOrchestrator
            orch = DeploymentOrchestrator()
            orch.health_check(instance.id)
        except Exception as exc:
            logger.debug("suppressed error in deploy_status (app/modules/codegen/routes/deploy_routes.py): %s", exc)

    return jsonify({
        "status": instance.health_status,
        "deployment_url": instance.deployment_url,
        "version": instance.version,
        "deployed_at": instance.deployed_at.isoformat() if instance.deployed_at else None,
        "last_health_check": instance.last_health_check.isoformat() if instance.last_health_check else None,
        "instance_id": instance.id,
    })


@codegen_bp.route(
    "/solutions/<int:solution_id>/codegen/deploy-cloud", methods=["DELETE"]
)
@login_required
def deploy_destroy(solution_id):
    """Stop and remove a deployed solution."""
    solution = Solution.query.get_or_404(solution_id)
    _check_access(solution)

    from app.modules.codegen.services.instance_tracker import InstanceTracker
    from app.modules.codegen.services.deployment_orchestrator import DeploymentOrchestrator

    tracker = InstanceTracker()
    instance = tracker.get_instance(solution_id=solution_id)
    if not instance:
        return jsonify({"error": "No deployment found."}), 404

    try:
        orch = DeploymentOrchestrator()
        orch.destroy(instance.id)
        return jsonify({"status": "stopped", "message": "Deployment destroyed."})
    except Exception as e:
        logger.exception("Failed to destroy deployment for solution %s", solution_id)
        return jsonify({"error": f"Destroy failed: {e}"}), 500


@codegen_bp.route(
    "/solutions/<int:solution_id>/codegen/recommend-language", methods=["GET"]
)
@login_required
def recommend_language_route(solution_id):
    """Recommend the best programming language for this solution."""
    solution = Solution.query.get_or_404(solution_id)
    _check_access(solution)

    from app.modules.codegen.services.deployment_orchestrator import recommend_language

    elements = []
    if hasattr(solution, "archimate_elements"):
        for junction in solution.archimate_elements:
            el = junction.archimate_element if hasattr(junction, "archimate_element") else None
            if el:
                props = {}
                if hasattr(junction, "spec_data") and isinstance(junction.spec_data, dict):
                    props = junction.spec_data
                elements.append({
                    "type": el.element_type if hasattr(el, "element_type") else "",
                    "properties": props,
                })

    recommendation = recommend_language(elements)
    return jsonify(recommendation)
