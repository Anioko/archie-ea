class WorkflowPipeline(db.Model):
    """
    Next-generation workflow-based pipeline with LangGraph.
    Completely separate from GenerationPipeline for zero risk.
    """

    __tablename__ = "workflow_pipelines"

    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(200), nullable=False)
    description = db.Column(db.Text)

    # Workflow definition (user-customizable graph)
    workflow_graph = db.Column(db.JSON, nullable=False)  # LangGraph JSON definition
    workflow_version = db.Column(db.Integer, default=1)

    # Template tracking
    template_id = db.Column(db.Integer, db.ForeignKey("workflow_templates.id"))
    is_template_instance = db.Column(db.Boolean, default=False)

    # Execution state
    status = db.Column(
        db.String(20), default="draft"
    )  # draft, pending, running, paused, completed, failed, cancelled
    current_node_id = db.Column(db.String(50))  # Currently executing node
    workflow_state = db.Column(db.JSON)  # Full LangGraph state for resume capability

    # Input configuration (similar to GenerationPipeline for compatibility)
    capability_ids = db.Column(db.Text)  # Comma-separated BusinessCapability IDs
    jira_project_id = db.Column(db.Integer, db.ForeignKey("jira_projects.id"))
    selected_issue_keys = db.Column(db.Text)  # Comma-separated JIRA keys
    platform_type_id = db.Column(db.Integer, db.ForeignKey("platform_types.id"))
    platform_config_id = db.Column(db.Integer, db.ForeignKey("platform_configurations.id"))
    technology_stack_id = db.Column(db.Integer, db.ForeignKey("technology_stacks.id"))

    # Cost tracking and budget enforcement
    budget_limit = db.Column(db.Float)  # Max $ to spend on LLM calls
    cost_accumulated = db.Column(db.Float, default=0.0)
    cost_alert_threshold = db.Column(db.Float)  # Alert when cost reaches this amount
    stop_on_budget_exceeded = db.Column(db.Boolean, default=True)

    # Quality gates
    require_human_approval = db.Column(db.Boolean, default=False)
    approval_stage_node_id = db.Column(db.String(50))  # Node where approval is required
    approved_by_id = db.Column(db.Integer, db.ForeignKey("users.id"))
    approved_at = db.Column(db.DateTime)

    # Execution metadata
    execution_mode = db.Column(
        db.String(20), default="sequential"
    )  # sequential, parallel, adaptive
    max_retry_attempts = db.Column(db.Integer, default=3)
    timeout_minutes = db.Column(db.Integer, default=60)

    # Output artifacts (generated by workflow)
    architecture_id = db.Column(db.Integer, db.ForeignKey("architecture_models.id"))
    output_artifacts = db.Column(db.JSON)  # Links to generated code, requirements, etc.

    # Background job tracking
    job_id = db.Column(db.String(50))  # Flask-RQ2 job ID for async execution

    # Timestamps
    created_at = db.Column(db.DateTime, default=datetime.utcnow)
    started_at = db.Column(db.DateTime)
    completed_at = db.Column(db.DateTime)
    created_by_id = db.Column(db.Integer, db.ForeignKey("users.id"), nullable=False)

    # Relationships
    nodes = db.relationship(
        "WorkflowNode", backref="pipeline", cascade="all, delete-orphan", lazy="dynamic"
    )
    edges = db.relationship(
        "WorkflowEdge", backref="pipeline", cascade="all, delete-orphan", lazy="dynamic"
    )
    executions = db.relationship(
        "WorkflowExecution", backref="pipeline", cascade="all, delete-orphan", lazy="dynamic"
    )

    # Foreign key relationships
    platform_type = db.relationship("PlatformType")
    platform_config = db.relationship("PlatformConfiguration")
    technology_stack = db.relationship("TechnologyStack")
    template = db.relationship(
        "WorkflowTemplate", foreign_keys=[template_id], overlaps="source_template,instances"
    )
    created_by = db.relationship("User", foreign_keys=[created_by_id])
    approved_by = db.relationship("User", foreign_keys=[approved_by_id])
    architecture = db.relationship("ArchitectureModel")

    @property
    def duration_seconds(self):
        """Calculate duration if pipeline has started and completed."""
        if self.started_at and self.completed_at:
            return int((self.completed_at - self.started_at).total_seconds())
        return None

    @property
    def budget_remaining(self):
        """Calculate remaining budget."""
        if self.budget_limit:
            return self.budget_limit - self.cost_accumulated
        return None

    @property
    def is_over_budget(self):
        """Check if pipeline has exceeded budget."""
        if self.budget_limit:
            return self.cost_accumulated >= self.budget_limit
        return False

    def __repr__(self):
        return f"<WorkflowPipeline {self.id}: {self.name}>"


