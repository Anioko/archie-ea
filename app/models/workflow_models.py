class WorkflowCleanupNode(db.Model):
    """
    Specialized node for cleaning up temporary files and artifacts.
    Automatically removes files based on patterns and retention policies.
    """

    __tablename__ = "workflow_cleanup_nodes"

    id = db.Column(db.Integer, primary_key=True)
    pipeline_id = db.Column(db.Integer, db.ForeignKey("workflow_pipelines.id"), nullable=False)

    # Cleanup configuration
    cleanup_patterns = db.Column(
        db.JSON
    )  # List of glob patterns to clean (e.g., ["temp/*", "artifacts/*.log"])
    retention_days = db.Column(db.Integer, default=7)  # Files older than this are deleted
    exclude_patterns = db.Column(db.JSON)  # Patterns to exclude from cleanup
    max_cleanup_size_mb = db.Column(db.Integer, default=100)  # Max size to clean in MB

    # Execution tracking
    files_cleaned = db.Column(db.Integer, default=0)
    space_freed_mb = db.Column(db.Float, default=0.0)
    cleanup_log = db.Column(db.JSON)  # Log of what was cleaned

    # Node identification (inherits from WorkflowNode)
    node_id = db.Column(db.String(50), nullable=False)
    label = db.Column(db.String(100), default="Cleanup")
    description = db.Column(db.Text, default="Clean up temporary files and artifacts")

    # Execution state
    status = db.Column(db.String(20), default="pending")
    started_at = db.Column(db.DateTime)
    completed_at = db.Column(db.DateTime)
    error_message = db.Column(db.Text)

    def execute_cleanup(self):
        """Execute the cleanup operation"""
        import glob
        import os
        from datetime import datetime, timedelta

        try:
            self.status = "running"
            self.started_at = datetime.utcnow()
            self.files_cleaned = 0
            self.space_freed_mb = 0.0
            self.cleanup_log = []

            cutoff_date = datetime.utcnow() - timedelta(days=self.retention_days)

            for pattern in self.cleanup_patterns or []:
                for file_path in glob.glob(pattern, recursive=True):
                    if self._should_exclude(file_path):
                        continue

                    try:
                        stat = os.stat(file_path)
                        file_date = datetime.fromtimestamp(stat.st_mtime)

                        if file_date < cutoff_date:
                            size_mb = stat.st_size / (1024 * 1024)
                            os.remove(file_path)

                            self.files_cleaned += 1
                            self.space_freed_mb += size_mb
                            self.cleanup_log.append(
                                {
                                    "file": file_path,
                                    "size_mb": size_mb,
                                    "deleted_at": datetime.utcnow().isoformat(),
                                }
                            )

                    except (OSError, FileNotFoundError) as e:
                        self.cleanup_log.append({"file": file_path, "error": str(e)})

            self.status = "completed"
            self.completed_at = datetime.utcnow()

        except Exception as e:
            self.status = "failed"
            self.error_message = str(e)
            self.completed_at = datetime.utcnow()

    def _should_exclude(self, file_path):
        """Check if file should be excluded from cleanup"""
        if not self.exclude_patterns:
            return False

        from fnmatch import fnmatch

        for pattern in self.exclude_patterns:
            if fnmatch(file_path, pattern):
                return True
        return False

    def __repr__(self):
        return f"<WorkflowCleanupNode {self.node_id}: cleaned {self.files_cleaned} files>"


