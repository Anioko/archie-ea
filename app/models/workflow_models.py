def _validate_workflow_status(target, value, oldvalue, initiator):
    """SQLAlchemy event listener that validates EAWorkflowInstance status transitions."""
    if oldvalue is value or oldvalue is None:
        return value
    if oldvalue is attributes.NO_VALUE or oldvalue is attributes.NEVER_SET:
        return value
    if oldvalue == value:
        return value
    allowed = _WORKFLOW_TRANSITIONS.get(oldvalue, set())
    if value not in allowed:
        _state_logger.warning(
            "Invalid EAWorkflowInstance transition blocked: %s -> %s (allowed: %s)",
            oldvalue, value, allowed or "none (terminal)",
        )
        raise ValueError(
            f"Invalid EAWorkflowInstance status transition: {oldvalue} -> {value}. "
            f"Allowed: {allowed or 'none (terminal state)'}"
        )
    _state_logger.debug("EAWorkflowInstance status: %s -> %s", oldvalue, value)
    return value


event.listen(EAWorkflowInstance.status, "set", _validate_workflow_status, retval=True)


